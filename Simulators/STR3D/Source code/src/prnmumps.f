      SUBROUTINE PRNMUMPS(NEQSOL,MALLOC,MAVBL,MREQ,ITO)
C
      INTEGER*8 MALLOC,MAVBL,MREQ
C
      WRITE(ITO,7439) 
 7439 FORMAT(/1X,5(1H*),2X,'MULTIFRONTAL SOLVER (MUMPS) MESSAGE',2X
     *       ,5(1H*) )
C
      WRITE(ITO,7449) NEQSOL, MALLOC*4.D-6, (MALLOC-MAVBL)*4.D-6, 
     *                MAVBL*4.D-6, MREQ*4.D-6
 7449 FORMAT(/3X,19HNUMBER OF EQUATIONS,1X,20(1H-),I8
     *       /3X,16HALLOCATED MEMORY,1X,23(1H-),F8.1,4H(MB)
     *       /3X,26HUSED MEMORY FOR DATA TABLE,1X,13(1H-),F8.1,4H(MB)
     *       /3X,34HAVAILABLE MEMORY FOR MATRIX SOLVER
     *       ,1X,5(1H-),F8.1,4H(MB)
     *       /3X,33HREQUIRED MEMORY FOR MATRIX SOLVER
     *       ,1X,6(1H-),F8.1,4H(MB) )
C
      RETURN
      END
